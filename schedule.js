// ⏰ GET GAME TIME
function getGameTime(game, group) {
  const match = game.field.match(/AT (\d+)(?::(\d+))?\s?(AM|PM)/i);

  if (match) {
    let hour = parseInt(match[1]);
    const min = match[2] || "00";
    const period = match[3].toUpperCase();

    if (period === "PM" && hour !== 12) hour += 12;
    if (period === "AM" && hour === 12) hour = 0;

    return `${hour.toString().padStart(2, "0")}:${min}`;
  }

  if (group === "U9") return "09:30";
  if (group === "U11") return "11:00";
  if (group === "U13") return "12:30";

  return "10:00";
}

// 📅 CALENDAR LINK
function createCalendarLink(game, team, opponent, isHome, group) {
  const year = "2026";
  const months = { Apr: "04", May: "05" };
  const [m, d] = game.date.split(" ");
  const date = `${year}${months[m]}${d.padStart(2, "0")}`;

  const startTime = getGameTime(game, group);

  // ⏱️ Duration (customizable)
  const duration = group === "U7" ? 45 : 60;

  const [startHour, startMin] = startTime.split(":").map(Number);

  const startDate = new Date(
    parseInt(year),
    parseInt(months[m]) - 1, // month is 0-based
    parseInt(d),
    startHour,
    startMin
  );

  const endDate = new Date(startDate.getTime() + duration * 60 * 1000);

  const format = (dt) =>
    dt.toISOString().replace(/[-:]/g, "").split(".")[0];

  const start = format(startDate);
  const end = format(endDate);

  // 📍 Clean field + address
  const cleanField = game.field.split(" AT ")[0];
  const address = fieldAddresses[cleanField] || game.field;

  // 👨‍🏫 Coaches
  const teamCoach = coaches[group][team] || "";
  const opponentCoach = opponent.startsWith("BEYS")
    ? coaches[group][opponent] || ""
    : "";

  // 📝 Title + description
  const title = `${team} vs ${opponent} (${isHome ? "HOME" : "AWAY"})`;

  const details = `
  ${team} - ${teamCoach}
  ${opponent}${opponentCoach ? " - " + opponentCoach : ""}
  
  Field: ${cleanField}
  Address: ${address}
    `.trim();

  return `https://www.google.com/calendar/render?action=TEMPLATE` +
    `&text=${encodeURIComponent(title)}` +
    `&dates=${start}/${end}` +
    `&location=${encodeURIComponent(address)}` +
    `&details=${encodeURIComponent(details)}`;
}

// 📍 MAP LINK (FIXED)
function createMapsLink(field) {
  const cleanField = field.split(" AT ")[0];
  const address = fieldAddresses[cleanField] || cleanField;
  return `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(address)}`;
}

// AGE CHANGE
ageGroup.addEventListener("change", () => {

  const group = ageGroup.value;
  teamSelect.innerHTML = `<option value="">Select Team</option>`;
  scheduleBody.innerHTML = "";

  if (!group) return;

  const teams = new Set();

  schedules[group].forEach(g => {
    if (g.home.startsWith("BEYS")) teams.add(g.home);
    if (g.away.startsWith("BEYS")) teams.add(g.away);
  });

  [...teams].sort().forEach(team => {
    const option = document.createElement("option");
    option.value = team;
    option.textContent = `${team} - ${coaches[group][team] || ""}`;
    teamSelect.appendChild(option);
  });

});

// TEAM CHANGE
teamSelect.addEventListener("change", () => {

  const group = ageGroup.value;
  const team = teamSelect.value;

  scheduleBody.innerHTML = "";

  if (!schedules[group]) return;

  schedules[group].forEach(game => {

    if (game.home === team || game.away === team) {

      const cleanField = game.field.toUpperCase();
      const isHome = cleanField.includes("BEYS");

      const opponent = game.home === team ? game.away : game.home;

      const teamCoach = coaches[group][team] || "";
      const opponentCoach = opponent.startsWith("BEYS")
        ? coaches[group][opponent] || ""
        : "";

      const calendarLink = createCalendarLink(game, team, opponent, isHome, group);
      const mapsLink = createMapsLink(game.field);

      const row = document.createElement("tr");
      row.className = isHome ? "home-row" : "away-row";

      row.innerHTML = `
      <td><span class="age-badge ${group.toLowerCase()}">${group}</span></td>
      <td>${game.date}</td>
    
      <td>
      <strong>${team}</strong><br>
      <small>${teamCoach}</small>
      </td>
    
      <td>
      ${opponent}
      ${opponentCoach ? `<br><small>${opponentCoach}</small>` : ""}
      </td>
    
      <td class="${isHome ? "home-text" : "away-text"}">
      ${isHome ? "HOME" : "AWAY"}
      </td>
    
      <td>
      <a href="${mapsLink}" target="_blank" class="field-link">
      ${game.field}
      </a>
      </td>
    
      <td>
      <a href="${calendarLink}" target="_blank" class="calendar-btn-small">
        <img src="calendaricon.png" alt="Add to Calendar">
      </a>
    </td>
      `;

      scheduleBody.appendChild(row);

    }

  });

});
